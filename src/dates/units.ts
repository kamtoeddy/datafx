export type Year = "y" | "Y";
export type Month = "mo" | "M";
export type Week = "w" | "W";
export type Day = "d" | "D";
export type Hour = "h" | "H";
export type Minute = "m";
export type Second = "s";
export type Millisecond = "ms";

export type DateTimeUnit =
  | Year
  | Month
  | Week
  | Day
  | Hour
  | Minute
  | Second
  | Millisecond;
